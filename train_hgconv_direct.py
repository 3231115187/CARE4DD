class PreparedInputs:
    pass


def prepare_inputs(args: argparse.Namespace, device: torch.device) -> PreparedInputs:
    raw = load_raw_graph_package(args.dataset_path, split_name=args.split_name)

    n_patient = raw["patient_x"].size(0)
    n_drug = raw["drug_x"].size(0)
    n_proc = raw["proc_x"].size(0)

    pd_edge = get_patient_entity_edges(raw["pkg"], "Drug", n_patient, n_drug)
    pr_edge = get_patient_entity_edges(raw["pkg"], "Procedure", n_patient, n_proc)

    nd, vd, np_, vp = load_dual_ppr_robust(args.ppr_dir, alpha=args.alpha, data_name=args.data)

    # Match the original CARE-4DD patch allocation:
    # patch_size=64 -> 1 patient + 31 drug + 32 procedure.
    k_drug = max(0, args.patch_size // 2 - 1)
    k_proc = int(args.patch_size) - 1 - k_drug
    if k_drug <= 0 or k_proc <= 0:
        raise ValueError(f"Invalid patch_size={args.patch_size}; derived k_drug={k_drug}, k_proc={k_proc}")

    topk_drug, score_drug = map_ppr_for_patients(nd, vd, n_patient, n_drug, n_proc, "drug", k_drug)
    topk_proc, score_proc = map_ppr_for_patients(np_, vp, n_patient, n_drug, n_proc, "proc", k_proc)

    obj = PreparedInputs()
    obj.x_patient = raw["patient_x"].to(device)
    obj.x_drug = raw["drug_x"].to(device)
    obj.x_proc = raw["proc_x"].to(device)
    obj.y = raw["y"].to(device)
    obj.train_mask = raw["train_mask"].to(device)
    obj.val_mask = raw["val_mask"].to(device)
    obj.test_mask = raw["test_mask"].to(device)
    obj.num_classes = int(raw["num_classes"])
    obj.patient_drug_edge_index = pd_edge.to(device)
    obj.patient_proc_edge_index = pr_edge.to(device)
    obj.topk_drug = topk_drug.to(device)
    obj.topk_proc = topk_proc.to(device)
    obj.score_drug = score_drug.to(device)
    obj.score_proc = score_proc.to(device)
    obj.pt_path = raw["pt_path"]
    obj.k_drug = k_drug
    obj.k_proc = k_proc
    return obj


def make_model(args: argparse.Namespace, inp: PreparedInputs, device: torch.device) -> CARE4DD_HGConv:
    cfg = CARE4DDHGConvConfig(
        patient_in_dim=int(inp.x_patient.size(1)),
        drug_in_dim=int(inp.x_drug.size(1)),
        proc_in_dim=int(inp.x_proc.size(1)),
        num_classes=int(inp.num_classes),
        hidden_dim=int(args.hidden_dim),
        num_drug_tokens=int(inp.k_drug),
        num_proc_tokens=int(inp.k_proc),
        encoder_layers=int(args.encoder_layers),
        token_mlp_dim=None,
        channel_mlp_dim=int(args.hidden_dim) * int(args.mixer_expansion),
        dropout=float(args.dropout),
        use_hgconv=bool(args.use_hgconv),
        use_type_embedding=True,
    )
    return CARE4DD_HGConv(cfg).to(device)


def forward_batch(model: CARE4DD_HGConv, inp: PreparedInputs, patient_ids: torch.Tensor, return_embedding: bool = False):
    patient_ids = patient_ids.to(inp.y.device).long()
    return model(
        x_patient=inp.x_patient,
        x_drug=inp.x_drug,
        x_proc=inp.x_proc,
        patient_drug_edge_index=inp.patient_drug_edge_index,
        patient_proc_edge_index=inp.patient_proc_edge_index,
        batch_patient_ids=patient_ids,
        topk_drug_ids=inp.topk_drug[patient_ids],
        topk_proc_ids=inp.topk_proc[patient_ids],
        return_embedding=return_embedding,
    )


@torch.no_grad()
def evaluate(model: CARE4DD_HGConv, inp: PreparedInputs, indices: torch.Tensor, batch_size: int) -> Tuple[Dict[str, float], torch.Tensor, torch.Tensor]:
    model.eval()
    logits_all = []
    y_all = []
    for start in range(0, int(indices.numel()), int(batch_size)):
        ids = indices[start:start + int(batch_size)]
        logits = forward_batch(model, inp, ids, return_embedding=False)
        logits_all.append(logits.detach().cpu())
        y_all.append(inp.y[ids].detach().cpu())
    logits_all = torch.cat(logits_all, dim=0)
    y_all = torch.cat(y_all, dim=0)
    return metrics_from_logits(logits_all, y_all, inp.num_classes), logits_all, y_all


def train_one_run(args: argparse.Namespace, inp: PreparedInputs, run_id: int, device: torch.device) -> Dict[str, Any]:
    seed = int(args.seed) + int(run_id)
    set_seed(seed)
    model = make_model(args, inp, device)

    train_indices = torch.where(inp.train_mask.detach().cpu())[0]
    val_indices = torch.where(inp.val_mask.detach().cpu())[0]
    test_indices = torch.where(inp.test_mask.detach().cpu())[0]

    loader = DataLoader(
        PatientIndexDataset(train_indices, inp.y.detach().cpu()),
        batch_size=int(args.batch_size),
        shuffle=True,
        num_workers=0,
    )

    class_weight = None
    if args.use_class_weight:
        class_weight = build_class_weight(inp.y, inp.train_mask, inp.num_classes, args.class_weight_power, device)
        print(f"[Run {run_id}] class weights: {class_weight.detach().cpu().numpy().round(4).tolist()}", flush=True)

    criterion = nn.CrossEntropyLoss(weight=class_weight, label_smoothing=float(args.label_smoothing)).to(device)
    optimizer = torch.optim.AdamW(model.parameters(), lr=float(args.lr), weight_decay=float(args.weight_decay))
    scheduler = torch.optim.lr_scheduler.CosineAnnealingLR(optimizer, T_max=max(1, int(args.epochs)))

    best = {
        "epoch": -1,
        "val": {"macro": -1.0},
        "test": None,
        "state_dict": None,
    }
    bad = 0

    for epoch in range(1, int(args.epochs) + 1):
        model.train()
        total_loss = 0.0
        total_n = 0
        for batch_ids, batch_y in loader:
            batch_ids = batch_ids.to(device).long()
            batch_y = batch_y.to(device).long()
            optimizer.zero_grad(set_to_none=True)
            logits = forward_batch(model, inp, batch_ids, return_embedding=False)
            loss = criterion(logits, batch_y)
            loss.backward()
            if float(args.grad_clip) > 0:
                torch.nn.utils.clip_grad_norm_(model.parameters(), float(args.grad_clip))
            optimizer.step()
            total_loss += float(loss.item()) * int(batch_ids.numel())
            total_n += int(batch_ids.numel())
        scheduler.step()

        val_metrics, _, _ = evaluate(model, inp, val_indices.to(device), args.eval_batch_size)
        test_metrics, _, _ = evaluate(model, inp, test_indices.to(device), args.eval_batch_size)
        train_loss = total_loss / max(1, total_n)

        if val_metrics["macro"] > best["val"].get("macro", -1.0):
            best = {
                "epoch": int(epoch),
                "train_loss": float(train_loss),
                "val": val_metrics,
                "test": test_metrics,
                "state_dict": {k: v.detach().cpu().clone() for k, v in model.state_dict().items()},
            }
            bad = 0
            flag = "*"
        else:
            bad += 1
            flag = ""

        if epoch == 1 or epoch % int(args.print_every) == 0 or flag:
            print(
                f"[Run {run_id}] Epoch {epoch:03d} | loss={train_loss:.4f} | "
                f"val micro/macro={val_metrics['micro']:.4f}/{val_metrics['macro']:.4f} | "
                f"test micro/macro={test_metrics['micro']:.4f}/{test_metrics['macro']:.4f} {flag}",
                flush=True,
            )
        if bad >= int(args.patience):
            print(f"[Run {run_id}] Early stop at epoch {epoch}, best epoch={best['epoch']}", flush=True)
            break

    if best["state_dict"] is not None:
        model.load_state_dict(best["state_dict"])
    final_test, test_logits, test_y = evaluate(model, inp, test_indices.to(device), args.eval_batch_size)

    # Optional t-SNE artifact for run 0.
    tsne_info = None
    if args.export_tsne and int(run_id) == int(args.tsne_run_id):
        model.eval()
        emb_list, y_list, pred_list, pid_list = [], [], [], []
        with torch.no_grad():
            for start in range(0, int(test_indices.numel()), int(args.eval_batch_size)):
                ids = test_indices[start:start + int(args.eval_batch_size)].to(device)
                logits, emb = forward_batch(model, inp, ids, return_embedding=True)
                emb_list.append(emb.detach().cpu().numpy())
                y_list.append(inp.y[ids].detach().cpu().numpy())
                pred_list.append(logits.argmax(dim=1).detach().cpu().numpy())
                pid_list.append(ids.detach().cpu().numpy())
        emb_np = np.concatenate(emb_list, axis=0)
        y_np = np.concatenate(y_list, axis=0)
        pred_np = np.concatenate(pred_list, axis=0)
        pid_np = np.concatenate(pid_list, axis=0)
        out_dir = Path(args.tsne_out_dir)
        ensure_dir(out_dir)
        out_path = out_dir / "care4dd_hgconv_test_embed.npz"
        np.savez_compressed(out_path, embeddings=emb_np.astype(np.float32), labels=y_np.astype(np.int64), predictions=pred_np.astype(np.int64), patient_ids=pid_np)
        out_path.with_suffix(".meta.json").write_text(json.dumps({
            "model_name": "CARE-4DD-HGConv" if args.use_hgconv else "CARE-4DD-noHGConv",
            "dataset_name": args.tsne_dataset_name,
            "num_samples": int(len(y_np)),
            "embedding_dim": int(emb_np.shape[1]),
        }, ensure_ascii=False, indent=2), encoding="utf-8")
        tsne_info = str(out_path)
        print(f"[t-SNE] Saved: {out_path}", flush=True)

    return {
        "run_id": int(run_id),
        "seed": int(seed),
        "best_epoch": int(best["epoch"]),
        "best_val": best["val"],
        "best_test": best["test"],
        "final": {"best_test": final_test},
        "final_stage": "stage1_hgconv" if args.use_hgconv else "stage1_no_hgconv",
        "tsne_artifact": tsne_info,
    }


def aggregate_results(results: List[Dict[str, Any]]) -> Dict[str, float]:
    def arr(metric: str) -> np.ndarray:
        return np.array([float(r["final"]["best_test"][metric]) for r in results], dtype=float)
    out = {}
    for key, paper_key in [
        ("micro", "final_test_micro"),
        ("macro", "final_test_macro"),
        ("auroc", "final_test_auroc"),
        ("auprc", "final_test_auprc"),
        ("accuracy", "final_test_accuracy"),
    ]:
        vals = arr(key)
        out[f"{paper_key}_mean"] = float(vals.mean())
        out[f"{paper_key}_std"] = float(vals.std(ddof=1)) if len(vals) > 1 else 0.0
    return out


