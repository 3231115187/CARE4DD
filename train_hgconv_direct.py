class PatientIndexDataset(Dataset):
    def __init__(self, indices: torch.Tensor, y: torch.Tensor):
        self.indices = indices.detach().cpu().long()
        self.y = y.detach().cpu().long()

    def __len__(self) -> int:
        return int(self.indices.numel())

    def __getitem__(self, i: int) -> Tuple[torch.Tensor, torch.Tensor]:
        idx = self.indices[i]
        return idx, self.y[idx]


def safe_macro_ovr_auc_ap(y_true: np.ndarray, prob: np.ndarray, num_classes: int) -> Tuple[float, float]:
    aucs, aps = [], []
    for c in range(int(num_classes)):
        binary_true = (y_true == c).astype(int)
        if binary_true.sum() == 0 or binary_true.sum() == len(binary_true):
            continue
        score = prob[:, c]
        try:
            aucs.append(float(roc_auc_score(binary_true, score)))
        except Exception:
            pass
        try:
            aps.append(float(average_precision_score(binary_true, score)))
        except Exception:
            pass
    return float(np.mean(aucs)) if aucs else 0.0, float(np.mean(aps)) if aps else 0.0


def metrics_from_logits(logits: torch.Tensor, y: torch.Tensor, num_classes: int) -> Dict[str, float]:
    logits_cpu = logits.detach().cpu()
    y_cpu = y.detach().cpu().long()
    true = y_cpu.numpy().astype(int)
    pred = logits_cpu.argmax(dim=1).numpy().astype(int)
    prob = torch.softmax(logits_cpu, dim=1).numpy()

    acc = float(accuracy_score(true, pred))
    micro = float(f1_score(true, pred, average="micro", zero_division=0))
    macro = float(f1_score(true, pred, average="macro", zero_division=0))
    auroc, auprc = safe_macro_ovr_auc_ap(true, prob, num_classes)
    return {"accuracy": acc, "micro": micro, "macro": macro, "auroc": auroc, "auprc": auprc}


def build_class_weight(y: torch.Tensor, train_mask: torch.Tensor, num_classes: int, power: float, device: torch.device) -> torch.Tensor:
    counts = torch.bincount(y[train_mask].long(), minlength=num_classes).float().to(device).clamp_min(1.0)
    weight = counts.pow(-float(power))
    weight = weight / weight.mean()
    return weight


