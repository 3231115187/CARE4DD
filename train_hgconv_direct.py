def main() -> None:
    args = parse_args()
    device = select_device(args)
    print("=" * 100, flush=True)
    print(f"Device: {device}", flush=True)
    print(f"Use HGConv: {args.use_hgconv}", flush=True)
    print("=" * 100, flush=True)

    save_dir = Path(args.save_dir)
    ensure_dir(save_dir)

    # Save config early.
    (save_dir / "config.json").write_text(json.dumps(vars(args), ensure_ascii=False, indent=2), encoding="utf-8")

    # Load once; reused across runs.
    prepared = prepare_inputs(args, device)

    results = []
    start = time.perf_counter()
    for run_id in range(int(args.runs)):
        print("\n" + "=" * 100, flush=True)
        print(f"Run {run_id}/{int(args.runs)-1}", flush=True)
        print("=" * 100, flush=True)
        results.append(train_one_run(args, prepared, run_id, device))

    aggregate = aggregate_results(results)
    elapsed = time.perf_counter() - start

    summary = {
        "config": vars(args),
        "data": {
            "graph_pt": prepared.pt_path,
            "patient_shape": list(prepared.x_patient.shape),
            "drug_shape": list(prepared.x_drug.shape),
            "procedure_shape": list(prepared.x_proc.shape),
            "patient_drug_edges": int(prepared.patient_drug_edge_index.size(1)),
            "patient_procedure_edges": int(prepared.patient_proc_edge_index.size(1)),
            "k_drug": int(prepared.k_drug),
            "k_proc": int(prepared.k_proc),
        },
        "results": results,
        "aggregate": aggregate,
        "elapsed_sec": float(elapsed),
    }
    summary_path = save_dir / "summary.json"
    summary_path.write_text(json.dumps(to_jsonable(summary), ensure_ascii=False, indent=2), encoding="utf-8")

    print("\nSUMMARY", flush=True)
    print(f"Final Test Micro-F1: {aggregate['final_test_micro_mean']:.4f} ± {aggregate['final_test_micro_std']:.4f}", flush=True)
    print(f"Final Test Macro-F1: {aggregate['final_test_macro_mean']:.4f} ± {aggregate['final_test_macro_std']:.4f}", flush=True)
    print(f"Final Test AUROC:    {aggregate['final_test_auroc_mean']:.4f} ± {aggregate['final_test_auroc_std']:.4f}", flush=True)
    print(f"Final Test AUPRC:    {aggregate['final_test_auprc_mean']:.4f} ± {aggregate['final_test_auprc_std']:.4f}", flush=True)
    print(f"Saved summary to: {summary_path}", flush=True)


if __name__ == "__main__":
    main()
