def resolve_graph_pt(dataset_path: str, split_name: str = "active_split") -> Path:
    p = Path(dataset_path)
    if p.is_file():
        if p.name != "graph_raw_tensors.pt":
            raise FileNotFoundError(f"dataset_path is a file but not graph_raw_tensors.pt: {p}")
        return p
    candidates = [
        p / split_name / "graph_raw_tensors.pt",
        p / "active_split" / "graph_raw_tensors.pt",
        p / "graph_raw_tensors.pt",
    ]
    for c in candidates:
        if c.exists():
            return c
    raise FileNotFoundError("Cannot find graph_raw_tensors.pt. Tried:\n" + "\n".join(str(c) for c in candidates))


def get_2d_tensor_from_item(item: Dict[str, Any], preferred_key: str = "x") -> torch.Tensor:
    if preferred_key in item and torch.is_tensor(item[preferred_key]) and item[preferred_key].dim() == 2:
        return item[preferred_key].detach().cpu().float()
    for _, v in item.items():
        if torch.is_tensor(v) and v.dim() == 2:
            return v.detach().cpu().float()
    raise KeyError(f"Cannot find 2D feature tensor in keys={list(item.keys())}")


def get_supervision_tensor(sup: Dict[str, Any], key: str, ndim: Optional[int] = None) -> torch.Tensor:
    aliases = {
        "y": ["y", "label", "labels", "patient_y", "Y"],
        "train_mask": ["train_mask", "train", "train_masks", "TRAIN_MASK"],
        "val_mask": ["val_mask", "valid_mask", "validation_mask", "val", "VAL_MASK"],
        "test_mask": ["test_mask", "test", "TEST_MASK"],
    }
    for k in aliases.get(key, [key]):
        if k in sup and torch.is_tensor(sup[k]):
            t = sup[k]
            if ndim is None or t.dim() == ndim:
                return t.detach().cpu()
    # Fallback for labels.
    if key == "y":
        for _, v in sup.items():
            if torch.is_tensor(v) and v.dim() == 1 and v.dtype in (torch.int64, torch.long, torch.int32):
                return v.detach().cpu().long()
    raise KeyError(f"Cannot find supervision tensor '{key}'. supervision keys={list(sup.keys())}")


def load_raw_graph_package(dataset_path: str, split_name: str = "active_split") -> Dict[str, Any]:
    pt_path = resolve_graph_pt(dataset_path, split_name=split_name)
    print(f"[DirectLoader] Loading graph_raw_tensors.pt: {pt_path}", flush=True)
    pkg = torch_load(pt_path)
    if not isinstance(pkg, dict):
        raise ValueError(f"graph_raw_tensors.pt should be a dict, got {type(pkg)}")

    node_features = pkg.get("node_features") or pkg.get("nodes")
    if not isinstance(node_features, dict):
        raise KeyError(f"graph package has no node_features/nodes dict. keys={list(pkg.keys())}")

    for ntype in ["Patient", "Drug", "Procedure"]:
        if ntype not in node_features:
            raise KeyError(f"Missing node type '{ntype}'. Existing node types={list(node_features.keys())}")

    patient_x = get_2d_tensor_from_item(node_features["Patient"])
    drug_x = get_2d_tensor_from_item(node_features["Drug"])
    proc_x = get_2d_tensor_from_item(node_features["Procedure"])

    sup = pkg.get("supervision") or pkg.get("labels") or pkg.get("patient_supervision") or {}
    if not isinstance(sup, dict):
        raise KeyError("graph package supervision/labels is missing or not a dict.")

    y = get_supervision_tensor(sup, "y", ndim=1).long()
    train_mask = get_supervision_tensor(sup, "train_mask", ndim=1).bool()
    val_mask = get_supervision_tensor(sup, "val_mask", ndim=1).bool()
    test_mask = get_supervision_tensor(sup, "test_mask", ndim=1).bool()

    if y.numel() != patient_x.size(0):
        raise ValueError(f"y length {y.numel()} != Patient node count {patient_x.size(0)}")

    num_classes = int(y.max().item()) + 1

    print(
        "[DirectLoader] Node features | "
        f"Patient={tuple(patient_x.shape)}, Drug={tuple(drug_x.shape)}, Procedure={tuple(proc_x.shape)}, "
        f"classes={num_classes}",
        flush=True,
    )
    print(
        "[DirectLoader] Splits | "
        f"train={int(train_mask.sum())}, val={int(val_mask.sum())}, test={int(test_mask.sum())}",
        flush=True,
    )

    return {
        "pt_path": str(pt_path),
        "pkg": pkg,
        "patient_x": patient_x,
        "drug_x": drug_x,
        "proc_x": proc_x,
        "y": y,
        "train_mask": train_mask,
        "val_mask": val_mask,
        "test_mask": test_mask,
        "num_classes": num_classes,
    }


