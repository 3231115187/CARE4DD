    ap.add_argument("--runs", type=int, default=3)
    ap.add_argument("--seed", type=int, default=729)
    ap.add_argument("--epochs", type=int, default=80)
    ap.add_argument("--patience", type=int, default=12)
    ap.add_argument("--batch_size", type=int, default=512)
    ap.add_argument("--eval_batch_size", type=int, default=512)
    ap.add_argument("--lr", type=float, default=8e-4)
    ap.add_argument("--weight_decay", type=float, default=8e-3)
    ap.add_argument("--label_smoothing", type=float, default=0.08)
    ap.add_argument("--use_class_weight", action="store_true")
    ap.add_argument("--class_weight_power", type=float, default=0.6)
    ap.add_argument("--grad_clip", type=float, default=1.0)
