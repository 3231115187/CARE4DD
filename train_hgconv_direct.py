def load_array_file(path: Path) -> Any:
    suf = path.suffix.lower()
    if suf == ".npy":
        return np.load(path, allow_pickle=False)
    if suf == ".npz":
        z = np.load(path, allow_pickle=True)
        return {k: z[k] for k in z.files}
    if suf in {".pt", ".pth"}:
        return torch_load(path)
    return None


def as_numpy(x: Any) -> Optional[np.ndarray]:
    if isinstance(x, np.ndarray):
        return x
    if torch.is_tensor(x):
        return x.detach().cpu().numpy()
    return None


def flatten_arrays(obj: Any, prefix: str = "") -> List[Tuple[str, np.ndarray]]:
    arr = as_numpy(obj)
    if arr is not None:
        return [(prefix, arr)]
    out: List[Tuple[str, np.ndarray]] = []
    if isinstance(obj, dict):
        for k, v in obj.items():
            out.extend(flatten_arrays(v, f"{prefix}.{k}" if prefix else str(k)))
    elif isinstance(obj, (list, tuple)):
        for i, v in enumerate(obj):
            out.extend(flatten_arrays(v, f"{prefix}.{i}" if prefix else str(i)))
    return out


def load_dual_ppr_robust(ppr_dir: str, alpha: float = 0.5, data_name: str = "medical") -> Tuple[np.ndarray, np.ndarray, np.ndarray, np.ndarray]:
    root = Path(ppr_dir)
    if not root.exists():
        raise FileNotFoundError(f"ppr_dir does not exist: {ppr_dir}")

    # First try exact CARE-4DD / medical_ppr names.
    alpha_candidates = []
    for a in [str(alpha), str(round(alpha, 2)), f"{alpha:.2f}", f"{alpha:.1f}"]:
        if a not in alpha_candidates:
            alpha_candidates.append(a)

    def exact(suffix: str, kind: str) -> Optional[np.ndarray]:
        prefix = "top_indices" if kind == "neigh" else "top_values"
        for a in alpha_candidates:
            p = root / f"{prefix}_alpha_{a}_{data_name}_{suffix}.npy"
            if p.exists():
                return np.load(p)
        return None

    nd = exact("drug", "neigh")
    vd = exact("drug", "value")
    np_ = exact("proc", "neigh")
    vp = exact("proc", "value")

    if nd is not None and np_ is not None:
        if vd is None:
            vd = np.zeros_like(nd, dtype=np.float32)
        if vp is None:
            vp = np.zeros_like(np_, dtype=np.float32)
        print(f"[PPR] Loaded exact files | drug={nd.shape}, proc={np_.shape}", flush=True)
        return nd.astype(np.int64), vd.astype(np.float32), np_.astype(np.int64), vp.astype(np.float32)

    # Robust fallback.
    arrays: List[Tuple[str, np.ndarray]] = []
    for p in sorted(root.rglob("*")):
        if not p.is_file() or p.suffix.lower() not in {".npy", ".npz", ".pt", ".pth"}:
            continue
        try:
            arrays.extend(flatten_arrays(load_array_file(p), p.name))
        except Exception as exc:
            print(f"[WARN] Failed to read {p}: {exc}", flush=True)

    def find(kind: str, target: str) -> Optional[np.ndarray]:
        kind_words = [kind] if kind == "drug" else ["proc", "procedure"]
        neigh_words = ["neighbor", "neighbour", "indices", "index", "idx", "neigh"]
        value_words = ["value", "score", "weight", "prob", "ppr"]
        wanted = neigh_words if target == "neigh" else value_words
        best, best_score = None, -1
        for name, arr in arrays:
            if arr.ndim != 2:
                continue
            lname = name.lower()
            score = 0
            if any(w in lname for w in kind_words): score += 10
            if any(w in lname for w in wanted): score += 10
            if target == "neigh" and np.issubdtype(arr.dtype, np.integer): score += 5
            if target == "value" and np.issubdtype(arr.dtype, np.floating): score += 5
            if score > best_score:
                best, best_score = arr, score
        return best if best_score >= 15 else None

    nd, vd, np_, vp = find("drug", "neigh"), find("drug", "value"), find("proc", "neigh"), find("proc", "value")
    if nd is None or np_ is None:
        print("Available 2D PPR arrays:", flush=True)
        for name, arr in arrays:
            if arr.ndim == 2:
                print("  ", name, arr.shape, arr.dtype, flush=True)
        raise FileNotFoundError("Could not identify drug/procedure PPR neighbor arrays.")
    if vd is None:
        vd = np.zeros_like(nd, dtype=np.float32)
    if vp is None:
        vp = np.zeros_like(np_, dtype=np.float32)
    print(f"[PPR] Loaded robust files | drug={nd.shape}, proc={np_.shape}", flush=True)
    return nd.astype(np.int64), vd.astype(np.float32), np_.astype(np.int64), vp.astype(np.float32)


def map_ppr_for_patients(
    neigh: np.ndarray,
    values: np.ndarray,
    n_patient: int,
    n_drug: int,
    n_proc: int,
    target: str,
    k: int,
) -> Tuple[torch.Tensor, torch.Tensor]:
    if neigh.shape[0] < n_patient:
        raise ValueError(f"PPR rows {neigh.shape[0]} < patient nodes {n_patient}")

    neigh_p = neigh[:n_patient, :k].copy()
    val_p = values[:n_patient, :k].copy() if values is not None else np.zeros_like(neigh_p, dtype=np.float32)

    if target == "drug":
        local_valid = (neigh_p >= 0) & (neigh_p < n_drug)
        global_valid = (neigh_p >= n_patient) & (neigh_p < n_patient + n_drug)
        mapped = np.where(global_valid, neigh_p - n_patient, neigh_p)
        valid = local_valid | global_valid
        max_n = n_drug
    elif target == "proc":
        local_valid = (neigh_p >= 0) & (neigh_p < n_proc)
        proc_offset = n_patient + n_drug
        global_valid = (neigh_p >= proc_offset) & (neigh_p < proc_offset + n_proc)
        mapped = np.where(global_valid, neigh_p - proc_offset, neigh_p)
        valid = local_valid | global_valid
        max_n = n_proc
    else:
        raise ValueError(target)

    valid = valid & (mapped >= 0) & (mapped < max(max_n, 1))
    mapped = np.where(valid, mapped, -1).astype(np.int64)
    val_p = np.where(valid, val_p, 0.0).astype(np.float32)

    valid_rate = float(valid.mean()) if valid.size > 0 else 0.0
    print(f"[PPR] mapped {target} topK={k} | valid_rate={valid_rate:.4f}", flush=True)
    return torch.tensor(mapped, dtype=torch.long), torch.tensor(val_p, dtype=torch.float32)


