"""
train_hgconv_direct.py

Direct training script for CARE-4DD + lightweight heterogeneous graph convolution.

This script DOES NOT use my_data.py. It reads data directly from:
    <dataset_path>/active_split/graph_raw_tensors.pt
which is the GNN-ready output produced by data_pipeline_v3.

Expected raw graph package structure:
    pkg['node_features']['Patient']['x']
    pkg['node_features']['Drug']['x']
    pkg['node_features']['Procedure']['x']
    pkg['supervision']['y'], train_mask, val_mask, test_mask
    pkg['edges']['Patient-Drug']['edge_index'] or reverse relation
    pkg['edges']['Patient-Procedure']['edge_index'] or reverse relation

PPR files can be generated by medical_ppr.py and usually look like:
    top_indices_alpha_0.5_medical_drug.npy
    top_values_alpha_0.5_medical_drug.npy
    top_indices_alpha_0.5_medical_proc.npy
    top_values_alpha_0.5_medical_proc.npy

The model file care4dd_hgconv_model.py must be in the same project directory.
"""

