def normalize_edge_key(key: Any) -> str:
    if isinstance(key, (tuple, list)):
        return "-".join(str(x) for x in key)
    return str(key)


def canonical_tuple(edge_item: Dict[str, Any], fallback_key: Any) -> Tuple[str, str, str]:
    can = edge_item.get("canonical_etype") if isinstance(edge_item, dict) else None
    if can is not None and len(can) == 3:
        return str(can[0]), str(can[1]), str(can[2])
    text = normalize_edge_key(fallback_key)
    parts = text.split("-")
    if len(parts) == 2:
        return parts[0], "rel", parts[1]
    if len(parts) == 3:
        return parts[0], parts[1], parts[2]
    # weak fallback by keywords
    low = text.lower()
    if "patient" in low and "drug" in low:
        return "Patient", "rel", "Drug" if low.index("patient") < low.index("drug") else "Patient"
    if "patient" in low and ("procedure" in low or "proc" in low):
        return "Patient", "rel", "Procedure" if low.index("patient") < low.index("proc") else "Patient"
    return "", "rel", ""


def edge_item_to_patient_entity_edge(
    edge_item: Dict[str, Any],
    edge_key: Any,
    target_type: str,
    n_patient: int,
    n_target: int,
) -> Optional[torch.Tensor]:
    if not isinstance(edge_item, dict) or "edge_index" not in edge_item:
        return None
    ei = edge_item["edge_index"]
    if not torch.is_tensor(ei) or ei.dim() != 2:
        return None
    ei = ei.detach().cpu().long()
    if ei.size(0) != 2 and ei.size(1) == 2:
        ei = ei.t().contiguous()
    if ei.size(0) != 2:
        return None

    src_type, _, dst_type = canonical_tuple(edge_item, edge_key)
    key_text = normalize_edge_key(edge_key).lower()

    # Preferred: canonical etype.
    if src_type == "Patient" and dst_type == target_type:
        out = ei
    elif src_type == target_type and dst_type == "Patient":
        out = torch.stack([ei[1], ei[0]], dim=0)
    else:
        # Fallback from key name.
        if "patient" not in key_text:
            return None
        if target_type.lower() == "drug" and "drug" not in key_text:
            return None
        if target_type.lower() == "procedure" and not ("procedure" in key_text or "proc" in key_text):
            return None
        # If key starts with Patient, assume [patient, entity], otherwise reverse.
        if key_text.startswith("patient"):
            out = ei
        else:
            out = torch.stack([ei[1], ei[0]], dim=0)

    valid = (out[0] >= 0) & (out[0] < n_patient) & (out[1] >= 0) & (out[1] < n_target)
    out = out[:, valid].contiguous()
    return out


def get_patient_entity_edges(pkg: Dict[str, Any], target_type: str, n_patient: int, n_target: int) -> torch.Tensor:
    edges = pkg.get("edges") or pkg.get("edge_index_dict") or pkg.get("edge_data") or {}
    if not isinstance(edges, dict):
        raise KeyError("raw package has no dict-like edges/edge_index_dict/edge_data")

    candidates: List[torch.Tensor] = []
    for k, item in edges.items():
        key_text = normalize_edge_key(k).lower()
        if "patient" not in key_text:
            continue
        if target_type.lower() == "drug" and "drug" not in key_text:
            continue
        if target_type.lower() == "procedure" and not ("procedure" in key_text or "proc" in key_text):
            continue
        e = edge_item_to_patient_entity_edge(item, k, target_type, n_patient, n_target)
        if e is not None and e.numel() > 0:
            candidates.append(e)

    if not candidates:
        print(f"[WARN] No Patient-{target_type} edges found. Use empty edge_index.", flush=True)
        return torch.empty((2, 0), dtype=torch.long)

    out = torch.cat(candidates, dim=1)
    # Remove duplicated columns.
    if out.size(1) > 0:
        pair = out[0].numpy().astype(np.int64) * int(max(n_target, 1)) + out[1].numpy().astype(np.int64)
        _, idx = np.unique(pair, return_index=True)
        idx = np.sort(idx)
        out = out[:, torch.tensor(idx, dtype=torch.long)]

    print(f"[DirectLoader] Patient-{target_type} edges: {out.size(1)}", flush=True)
    return out.contiguous()


